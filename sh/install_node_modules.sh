# Install global node packages

cecho $green "\nChecking node gulp"

if ! type gulp > /dev/null 2>&1; then
	echo "sudo npm install -g gulp"
	sudo npm install -g gulp
else
	echo "gulp is available."
	echo `gulp -v`
fi

cecho $green "\nChecking node typescript"

if ! type tsc > /dev/null 2>&1; then
	echo "sudo npm install -g typescript"
	sudo npm install -g typescript
else
	echo "tsc is available."
	echo `tsc -v`
fi
